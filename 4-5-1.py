def multi_matrix(amount_rows, amount_cols):
    matrix_mul = []
    for r in range(amount_rows):
        res = []
        for c in range(amount_cols):
            res.append(r * c)
        matrix_mul.append(res)
    for string in range(amount_rows):
        print(*matrix_mul[string])


