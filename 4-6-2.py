def simple_nums(n_size):
    matrix = []
    for i in range(n_size):
        nums = []
        for j in range(n_size):
            if (i < j and i < n_size - 1 - j) or \
                    (i > j and i < n_size - 1 - j):
                nums.append(0)
            elif n_size == i + j + 1:
                nums.append(1)
            elif (i < j and i > n_size - 1 - j) or \
                    (i > j and i > n_size - 1 - j):
                nums.append(2)
            elif i == j and i + j < n_size:
                nums.append(0)
            else:
                nums.append(2)
        matrix.append(nums)

    for row in matrix:
        print(*row)


def main():
    n = int(input())
    return simple_nums(n)


main()
