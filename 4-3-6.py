def chunked(s, n):
    s_list, res_list, tmp_list = [], [], []
    cnt = 0

    for i in s:
        s_list.append(list(i))

    if n == 1:
        return s_list

    s_list.append([])

    for i in s_list:
        tmp_list += i
        cnt += 1
        if cnt == n:
            res_list.append(tmp_list)
            tmp_list = []
            cnt = 0
    return res_list


in_str = input().split(' ')
n_group = int(input())

print(chunked(in_str, n_group))